function addCarsByCategory(category, baza, listElement) {
  const cars = baza.filter((item) => item.category.toLowerCase() === category.toLowerCase());
  loadCars(listElement, cars);
}

addCarsByCategory("Registon", baza, ListSUV);
addCarsByCategory("Ulugbek", baza, ListSport);
addCarsByCategory("Sher-dor", baza, ListPremium);
addCarsByCategory("Gur-Amir", baza, ListComfort);

function loadCars(list, cars) {

  const loadMoreCars = function () {
    const carsHTML = cars
      .map((item) => {
        return `
          <div class="box__Item">
            <div class="box__item-Loc">
              <p>Samarqand</p>
            </div>
            <div class="box__item-Img">
              <img src="${item.img}" alt="${item.name}">
            </div>
            <h4 class="box__item-name">${item.name}</h4>
            <div class="box__item-description">
            <p>${item.info}</p>
            </div>
            
          </div>
        `;
      })
      .join("");

    list.innerHTML = carsHTML;
  };

  loadMoreCars();
}

